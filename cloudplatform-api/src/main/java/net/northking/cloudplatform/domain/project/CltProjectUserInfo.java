package net.northking.cloudplatform.domain.project;

public class CltProjectUserInfo {
    private String UserChnName;
    private String LoginNo;
    private String RoleName;
    private String OrgName;
    private String CellPhone;
    private String Email;
    private String userId;
    private String roleId;
    public String getUserChnName() {
        return UserChnName;
    }

    public void setUserChnName(String userChnName) {
        UserChnName = userChnName;
    }

    public String getLoginNo() {
        return LoginNo;
    }

    public void setLoginNo(String loginNo) {
        LoginNo = loginNo;
    }

    public String getRoleName() {
        return RoleName;
    }

    public void setRoleName(String roleName) {
        RoleName = roleName;
    }

    public String getOrgName() {
        return OrgName;
    }

    public void setOrgName(String orgName) {
        OrgName = orgName;
    }

    public String getCellPhone() {
        return CellPhone;
    }

    public void setCellPhone(String cellPhone) {
        CellPhone = cellPhone;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getRoleId() {
        return roleId;
    }

    public void setRoleId(String roleId) {
        this.roleId = roleId;
    }

    @Override
    public String toString() {
        return "CltProjectUserInfo{" +
                "UserChnName='" + UserChnName + '\'' +
                ", LoginNo='" + LoginNo + '\'' +
                ", RoleName='" + RoleName + '\'' +
                ", OrgName='" + OrgName + '\'' +
                ", CellPhone='" + CellPhone + '\'' +
                ", Email='" + Email + '\'' +
                ", userId='" + userId + '\'' +
                ", roleId='" + roleId + '\'' +
                '}';
    }
}
